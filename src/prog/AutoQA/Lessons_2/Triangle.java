package prog.AutoQA.Lessons_2;

public class Triangle {
    public static void main(String[] args) {
        for (int i = 1; i <11 ; i++) {
            int j = i;
            while (j > 0) {
                System.out.print("*");
                j--;
            }
            System.out.print("\n");
        }
    }
}

